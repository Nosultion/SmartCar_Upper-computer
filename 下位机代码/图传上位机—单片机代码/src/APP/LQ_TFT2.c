/*!
* @brief 画圆
*
* @param x ：圆心x   (0-127)
* @param y ：圆心y   (0-159)
* @param r ：半径 (0-128)
* @param color_dat ：颜色
*
* @return   无
*
* @note  圆心坐标不要超出屏幕范围
*
* @see   TFT2SPI_Draw_Circle(50, 50, 30, u16YELLOW);
*
* @date  2019/6/13 星期四
*/
void TFT2SPI_Draw_Circle(unsigned char x,unsigned char y,unsigned char r,unsigned short color_dat)
{
  unsigned char dx, dy = r; 
  
  if((x>=r) &&((TFT20W-x)>=r) && (y>=r) && ((TFT20H-y)>=r))		//确定所画圆在屏幕范围内，没有超出最外边，(暂不支持与屏幕边相交)
  {
    for(dx = 0; dx <= r; dx++)
    {
      while((r * r + 1 - dx * dx) < (dy * dy)) dy--;
      TFT2SPI_Draw_Dot(x + dx, y - dy, color_dat);
      TFT2SPI_Draw_Dot(x - dx, y - dy, color_dat);
      TFT2SPI_Draw_Dot(x - dx, y + dy, color_dat);
      TFT2SPI_Draw_Dot(x + dx, y + dy, color_dat);
      
      TFT2SPI_Draw_Dot(x + dy, y - dx, color_dat);
      TFT2SPI_Draw_Dot(x - dy, y - dx, color_dat);
      TFT2SPI_Draw_Dot(x - dy, y + dx, color_dat);
      TFT2SPI_Draw_Dot(x + dy, y + dx, color_dat);
    }
  }
}


/*!
* @brief 画线
*
* @param xs ：起始x
* @param ys ：起始y
* @param xe ：结束x
* @param ys ：结束y
* @param color_dat ：颜色
*
* @return   无
*
* @note  起始、终止横坐标(0-127)，纵坐标(0-159),显示颜色uint16
*
* @see   TFT2SPI_Draw_Line(10, 20, 30, 40, u16YELLOW);
*
* @date  2019/6/13 星期四
*/
void TFT2SPI_Draw_Line(unsigned char xs,unsigned char ys,unsigned char xe,unsigned char ye,unsigned short color_dat)
{
  int i,ds;
  int dx,dy,inc_x, inc_y;
  int xerr = 0, yerr = 0;				  //初始化变量
  
  if(xs==xe)	  						//如果是画垂直线则只需对竖直坐标计数
  {
    TFT2SPI_Set_Pos(xs,ys,xe,ye);
    for(i=0;i<(ye-ys+1);i++)
    {
      TFT2SPI_Write_Word(color_dat);
    }
  }
  else if(ys==ye)						//如果是水平线则只需要对水平坐标计数
  {
    TFT2SPI_Set_Pos(xs,ys,xe,ye);
    for(i=0;i<(xe-xs+1);i++)
    {
      TFT2SPI_Write_Word(color_dat);
    }
  } 
  else								//如果是斜线，则重新计算，使用画点函数画出直线
  {
    dx = xe - xs;						//计算坐标增量
    dy = ye - ys;
    
    if(dx > 0) inc_x = 1;					//设置单步方向
    else
    {
      inc_x = -1; dx = -dx;
    }
    if(dy > 0) inc_y = 1;					//设置单步方向
    else
    {
      inc_y = -1; dy = -dy;
    }
    
    if(dx > dy) {ds = dx;}					//选取基本增量坐标轴
    else		{ds = dy;}
    
    for(i = 0; i <= ds+1; i++)					//画线输出
    {
      TFT2SPI_Draw_Dot(xs, ys,color_dat);	//画点
      xerr += dx;
      yerr += dy;
      if(xerr > ds)
      {
        xerr -= ds;
        xs   += inc_x;
      }
      if(yerr > ds)
      {
        yerr -= ds;
        ys   += inc_y;
      }
    }
  }  
}

