/**
* @brief 不精确延时
*
* @param 无
*
* @return   无
*
* @note  无
*
* @see   lq_TFT2_delayms(100);
*
* @date  2019/4/22 星期一
*/
static void lq_TFT2_delayms(unsigned short ms)
{
  volatile unsigned long i = 0;
  while(ms--)
  {
    for (i = 0; i < 15000; ++i)
    {
      __asm("NOP"); /* delay */
    }
  }
}

#define TFT2SPI_CS_H  PIN_Write(TFT2SPI_CS, 1);   /*!< CS管脚 */
